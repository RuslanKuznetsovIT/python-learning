# Это твоя рабочая логика из новой системы мотивации (rolling 6-month window).

# Твой код ниже:

term = int(input("Сколько клиент с нами? "))
amount = int(input("Сумма ежемесячного платежа "))


if term >= 6:
    print("Передать ПМу")
elif amount >= 100000:
    print("Передать ПМу (крупный клиент)")
else:
    print("Оставить у МОПа")
