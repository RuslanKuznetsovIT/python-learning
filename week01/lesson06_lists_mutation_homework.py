# 1. Создай список clients с тремя клиентами:
#    ["Иванов", "Петрова", "Сидоров"]
#
# 2. Выведи стартовый список с подписью "Утром в воронке:"
#
# 3. Добавь нового клиента "Кузнецов" в конец (через append)
#    Выведи "После нового лида:" и список
#
# 4. Удали первого клиента (через pop с индексом 0). Сохрани имя удалённого
#    в переменную closed_client.
#    Выведи строку: "Закрыли сделку с: {closed_client}"
#
# 5. Поменяй имя клиента под индексом 0 (теперь это Петрова) на "Петрова Анна"
#    (полное имя). Выведи "После уточнения имени:" и список
#
# 6. Проверь, есть ли "Сидоров" в списке. Выведи строку:
#    "Сидоров в воронке? {True или False}"
#
# 7. Выведи итоговое количество клиентов: "Итого клиентов в воронке: {число}"
