# Ожидаемый вывод:
# Утром в воронке: ['Иванов', 'Петрова', 'Сидоров']
# После нового лида: ['Иванов', 'Петрова', 'Сидоров', 'Кузнецов']
# Закрыли сделку с: Иванов
# После уточнения имени: ['Петрова Анна', 'Сидоров', 'Кузнецов']
# Сидоров в воронке? True
# Итого клиентов в воронке: 3
#
# Дисциплина: запусти → сверь посимвольно → потом сдавай.

# Пиши код ниже:

clients = ["Иванов", "Петрова", "Сидоров"]
print(f"Утром в воронке: {clients}")

clients.append("Кузнецов")
print(f"После нового лида: {clients}")


closed_client = clients.pop(0)
print(f"Закрыли сделку с: {closed_client}")

clients[0] = "Петрова Анна"
print(f"После уточнения имени: {clients}")

print(f"Сидоров в воронке? {'Сидоров' in clients}")

print(f"Итого клиентов в воронке: {len(clients)}")