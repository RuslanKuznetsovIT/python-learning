# ЗАДАЧА 4 — функция с циклом внутри (сложная)
# ===============================================
# Описать функцию sum_to(n), которая возвращает сумму всех чисел от 1 до n
# включительно. Использовать ВНУТРИ функции цикл for + накопитель.
# Пример: sum_to(5) = 1+2+3+4+5 = 15
#
# Вызвать для трёх значений:
# - n = 10
# - n = 100
# - n = 5
#
# Ожидаемый вывод:
# Сумма от 1 до 10: 55
# Сумма от 1 до 100: 5050
# Сумма от 1 до 5: 15

# твой код:

def sum_to(n):
    total = 0
    for i in range(n+1):
        total += i
    return total

value_10 = sum_to(10)
print(f"Сумма от 1 до 10: {value_10}")
value_100 = sum_to(100)
print(f"Сумма от 1 до 100: {value_100}")
value_5 = sum_to(5)
print(f"Сумма от 1 до 5: {value_5}")