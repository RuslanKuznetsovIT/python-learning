# Описать функцию calculate_net_salary(gross), которая принимает
# зарплату до налога (gross) и возвращает зарплату после вычета НДФЛ 13%.
# Формула: gross - gross * 0.13  (или gross * 0.87 — то же самое)
#
# Вызвать для трёх сотрудников и вывести.
#
# Сотрудники:
# - Анна, зарплата 100000
# - Игорь, зарплата 75000
# - Светлана, зарплата 150000
#
# Ожидаемый вывод:
# Анна на руки: 87000.0
# Игорь на руки: 65250.0
# Светлана на руки: 130500.0

# твой код:

def calculate_net_salary(gross):
    salary = gross - gross * 0.13
    return salary

ann = calculate_net_salary(100000)
igor = calculate_net_salary(75000)
svetlana = calculate_net_salary(150000)

print(f"Анна на руки: {ann}")
print(f"Игорь на руки: {igor}")
print(f"Светлана на руки: {svetlana}")







