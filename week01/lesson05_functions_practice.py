# ЗАДАЧА 1 — простая функция (выручка по сделке)
# ===============================================
# Описать функцию calculate_revenue(price, quantity), которая возвращает
# произведение цены на количество (price * quantity).
# Вызвать её для трёх сделок и вывести результаты.
#
# Сделки:
# - Цена 1500, количество 4
# - Цена 8000, количество 2
# - Цена 350, количество 12
#
# Ожидаемый вывод:
# Выручка сделки 1: 6000
# Выручка сделки 2: 16000
# Выручка сделки 3: 4200

# твой код:

def calculate_revenue(price, quantity):
    revenue = price * quantity
    return revenue

deal_1 = calculate_revenue(1500,4)
deal_2 = calculate_revenue(8000,2)
deal_3 = calculate_revenue(350,12)

print(f"Выручка сделки 1: {deal_1}")
print(f"Выручка сделки 2: {deal_2}")
print(f"Выручка сделки 3: {deal_3}")




