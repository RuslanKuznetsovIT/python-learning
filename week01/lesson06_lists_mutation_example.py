# Урок 6 (часть 2) — изменение списков

leads = ["Иван Петров", "Ольга Сидорова", "Дмитрий Козлов"]
print(f"Утренний список лидов: {leads}")
print(f"Всего: {len(leads)}")

# Пришёл новый лид
leads.append("Светлана Зайцева")
print(f"После прихода нового: {leads}")

# Первый лид уже закрыли — убираем
closed = leads.pop(0)
print(f"Закрыли: {closed}")
print(f"Осталось в воронке: {leads}")

# Проверим, есть ли конкретный человек
print(f"Дмитрий в воронке? {'Дмитрий Козлов' in leads}")
print(f"Анна в воронке? {'Анна Иванова' in leads}")

# Меняем имя (например, клиент сменил контактное лицо)
leads[0] = "Ольга Сидорова (новый контакт)"
print(f"После изменения: {leads}")
