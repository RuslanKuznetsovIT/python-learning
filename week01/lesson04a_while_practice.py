# ЗАДАЧА 3 — зарплата менеджера с прогрессивным бонусом
#
# Спроси у пользователя через input(): сколько сделок закрыл менеджер за месяц.
# Не забудь обернуть в int()!
#
# Правила начисления зарплаты:
#   - Базовый оклад: 30 000 ₽ (это просто стартовая сумма)
#   - За каждую сделку — премия 5 000 ₽
#   - НО: с 6-й сделки и далее премия растёт до 8 000 ₽ за сделку
#
# Используй while: счётчик от 1 до количества сделок,
# и в теле — if/else чтобы выбрать размер премии (5000 или 8000).
#
#
# Проверь на 3 значениях:
#   - 3 сделки  → 30000 + 3*5000 = 45000 ₽
#   - 5 сделок  → 30000 + 5*5000 = 55000 ₽
#   - 10 сделок → 30000 + 5*5000 + 5*8000 = 95000 ₽
# ════════════════════════════════════════════════════

# Твой код здесь:


salary = 30000
deal = int(input("сколько сделок закрыл менеджер за месяц?"))
n = 1

while n <= deal:
    if n <= 5:
        salary += 5000
        n += 1
    else:
        salary += 8000
        n += 1
print(salary)




