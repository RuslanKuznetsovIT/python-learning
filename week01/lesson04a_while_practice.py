# ЗАДАЧА 2 — на какой день будет выполнен план
#
# План отдела: закрыть 30 встреч с клиентами.
# Каждый день отдел проводит 4 встречи.
# На какой день план будет ВЫПОЛНЕН или ПЕРЕВЫПОЛНЕН?
#
# Подсказка:
# - У тебя ДВА накопителя:
#     день = 0 (счётчик дней)
#     встречи = 0 (накопитель встреч)
# - Условие: пока встречи < 30
# - В теле цикла:
#     день += 1
#     встречи += 4
# - После выхода из цикла — вывести результат
#
# Главное отличие от прошлых задач:
# Условие проверяет НАКОПИТЕЛЬ, а не счётчик!
#
# Вывести через f-строку:
#   План выполнен на день 8. Всего встреч: 32
# ════════════════════════════════════════════════════

# Твой код здесь:

plan = 0
day = 0
plan_day = 4
while plan <= 30:
    plan += plan_day
    day += 1
print(f"План выполнен на день {day}. Всего встреч: {plan}")

