# Урок 5 — разогрев на циклах перед повтором функций
# Задание:
# Вывести таблицу скидок: каждое количество товаров даёт скидку в 5 раз больше.
# 1 товар = 5%, 2 товара = 10%, 3 = 15%, 4 = 20%, 5 = 25%.
#
# Ожидаемый вывод:
# 1 товаров — скидка 5%
# 2 товаров — скидка 10%
# 3 товаров — скидка 15%
# 4 товаров — скидка 20%
# 5 товаров — скидка 25%
#
# Подсказки:
# - цикл for с range от 1 до 6 (потому что 6 не включается)
# - внутри: считай скидку как количество * 5
# - print с f-строкой
#
# Дисциплина: запусти → сверь с эталоном → потом сдавай!

# Пиши код ниже:


for product in range(1,6):
    print(f"{product} товаров — скидка {product*5}%")