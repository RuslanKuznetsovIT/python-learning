# Урок 6 — списки (list)
# Задание:
# 1. Создай список deals с суммами пяти сделок: 50000, 120000, 35000, 80000, 95000
# 2. Выведи первую и последнюю сделку через индекс
# 3. Выведи количество сделок через len()
# 4. Через цикл for пройди по списку и выведи каждую сделку в формате "Сделка N: СУММА"
#    где N — порядковый номер начиная с 1 (не с 0!)
# 5. Через цикл for + накопитель посчитай ОБЩУЮ сумму всех сделок и выведи
#
# Ожидаемый вывод:
# Первая сделка: 50000
# Последняя сделка: 95000
# Всего сделок: 5
# Сделка 1: 50000
# Сделка 2: 120000
# Сделка 3: 35000
# Сделка 4: 80000
# Сделка 5: 95000
# Общая сумма: 380000
#
# Дисциплина: запусти → сверь посимвольно с эталоном → потом сдавай.

# Пиши код ниже:

deals = [50000, 120000, 35000, 80000, 95000]
print(f"Первая сделка: {deals[0]}")
print(f"Последняя сделка: {deals[-1]}")
print(f"Всего сделок: {len(deals)}")

count = 0
for deal in deals:
    count += 1
    print(f"Сделка {count}: {deal}")


total = 0
for deal in deals:
    total = total + deal

print(f"Общая сумма: {total}")