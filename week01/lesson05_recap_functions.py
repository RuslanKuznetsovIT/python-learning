# Задание:
# 1. Описать функцию calculate_bonus(base, percent), которая возвращает base * percent / 100
# 2. Вызвать её для трёх сотрудников и вывести результаты через print с f-строкой
#
# Сотрудники:
# Алексей — оклад 80000, процент 25
# Мария — оклад 60000, процент 30
# Виктор — оклад 120000, процент 15
#
# Ожидаемый вывод:
# Бонус Алексея: 20000.0
# Бонус Марии: 18000.0
# Бонус Виктора: 18000.0
#
# Дисциплина: запусти → сверь посимвольно с эталоном → потом сдавай.

# Пиши код ниже:

def calculate_bonus(base, percent):
    salary = base * percent / 100
    return salary

Alex = calculate_bonus(80000,25)
Mary = calculate_bonus(60000,30)
Viktor = calculate_bonus(120000,15)

print(f"Бонус Алексея: {Alex}")
print(f"Бонус Марии: {Mary}")
print(f"Бонус Виктора: {Viktor}")