# -------------------------------------------------------------
# В отделе 5 рабочих дней. Каждый день менеджер должен закрывать
# на 10 000 ₽ БОЛЬШЕ, чем в предыдущий. Старт — 50 000 ₽ в день 1.
#
# Выведи план в формате:
#   "День 1: план 50000 ₽"
#   "День 2: план 60000 ₽"
#   "День 3: план 70000 ₽"
#   "День 4: план 80000 ₽"
#   "День 5: план 90000 ₽"
#   "Итого за неделю: 350000 ₽"
#
# Подсказка: тут одновременно нужен счётчик дня (его даёт range)
# и накопитель итоговой суммы. Это связка из Примера 2.
# -------------------------------------------------------------

# твой код здесь:
total = 0
for day in range(1,6):
    plan = 50000 + (day * 10000 - 10000)
    total += plan
    print(f"День {day}: план {plan} ₽")
print(f"Итого за неделю: {total} ₽")


