# -------------------------------------------------------------
# ПРИМЕР 1 — Простой перебор от 1 до 5
# -------------------------------------------------------------
print("Пример 1: числа от 1 до 5")
for i in range(1, 6):       # range(1, 6) → 1, 2, 3, 4, 5
    print(i)
print()


# -------------------------------------------------------------
# ПРИМЕР 2 — Сумма с накопителем (как делал на 4a, но через for)
# -------------------------------------------------------------
print("Пример 2: сумма от 1 до 10")
total = 0
for n in range(1, 11):       # 1, 2, ..., 10  (11 НЕ включается!)
    total += n
print(f"Сумма от 1 до 10 = {total}")
print()


# -------------------------------------------------------------
# ПРИМЕР 3 — range с шагом
# -------------------------------------------------------------
print("Пример 3: чётные числа от 2 до 10")
for x in range(2, 11, 2):    # старт=2, стоп=11, шаг=2 → 2, 4, 6, 8, 10
    print(x)
print()


# -------------------------------------------------------------
# ПРИМЕР 4 — Обратный отсчёт (отрицательный шаг)
# -------------------------------------------------------------
print("Пример 4: обратный отсчёт от 5 до 1")
for i in range(5, 0, -1):    # старт=5, стоп=0, шаг=-1 → 5, 4, 3, 2, 1
    print(i)
print("Старт!")
print()


