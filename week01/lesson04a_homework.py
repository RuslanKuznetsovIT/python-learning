# ЗАДАЧА 2 — Комиссия менеджера
# -------------------------------------------------------------
# Спроси у пользователя оборот за месяц (через input).
# Правила:
#   - до 100 000 ₽ включительно → комиссия 10%
#   - свыше 100 000 ₽           → комиссия 15%
#
# Выведи через f-строку, например:
#   "Оборот 80000 ₽ → комиссия 8000.0 ₽ (ставка 10%)"
#   "Оборот 150000 ₽ → комиссия 22500.0 ₽ (ставка 15%)"
#
# Подсказка: тут циклов НЕ нужно. Это связка input + if/else + f-строка.
# Не забудь int() для оборота.
# -------------------------------------------------------------

# твой код здесь:

sales = int(input("Какой оборот в месяц?"))
if sales <= 100000:
    print(f"Оборот {sales} ₽ → комиссия {sales * 0.1} ₽ (ставка 10%)")
else:
    print(f"Оборот {sales} ₽ → комиссия {sales * 0.15} ₽ (ставка 15%)")