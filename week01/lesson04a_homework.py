# -------------------------------------------------------------
# ЗАДАЧА 1 — Сумма от 1 до 100
# -------------------------------------------------------------
# Используй цикл while.
# Посчитай сумму всех чисел от 1 до 100 (включительно).
# В конце выведи через f-строку:
#   "Сумма от 1 до 100 равна 5050"
#
# Подсказка: тебе нужны ДВЕ переменные —
#   одна для счётчика (текущее число),
#   вторая для накопителя (общая сумма).
# -------------------------------------------------------------

# твой код здесь:
n = 1
total = 0
while n <= 100:
    total += n
    n += 1
print(f"Сумма чисел от 1 до 100: {total}")

