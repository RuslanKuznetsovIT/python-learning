# Урок 6 — списки (list)
# Пример: список менеджеров отдела продаж

# Создаём список
managers = ["Анна", "Сергей", "Виктор", "Мария", "Дмитрий"]

# Доступ по индексу
print(f"Первый менеджер: {managers[0]}")
print(f"Третий менеджер: {managers[2]}")
print(f"Последний менеджер: {managers[-1]}")

# Длина списка
print(f"Всего менеджеров: {len(managers)}")

# Перебор списка циклом for
print("Список отдела:")
for name in managers:
    print(f"  - {name}")
