# ------ БЛОК 1: Переменные ------
# Переменная — это коробка с именем, в которой хранится значение.
# Создаётся просто: имя = значение

имя = "Руслан"          # строка (текст)
возраст = 33            # целое число
рост = 1.82             # число с точкой (дробное)
есть_ребёнок = True     # булево (да/нет)

# ------ БЛОК 2: print ------
# print() — выводит что угодно на экран

print(имя)
print(возраст)
print(рост)
print(есть_ребёнок)

# Можно печатать несколько через запятую:
print("Привет,", имя, "! Тебе", возраст, "лет.")

# Или через f-строку (современный способ):
print(f"Привет, {имя}! Тебе {возраст} лет, рост {рост} м.")

# ------ БЛОК 3: Типы данных ------
# Python сам понимает тип, но ты должен знать:
#   str   — строка:        "текст"
#   int   — целое число:   42
#   float — дробное:       3.14
#   bool  — да/нет:        True / False

print(type(имя))        # <class 'str'>
print(type(возраст))    # <class 'int'>
print(type(рост))       # <class 'float'>
print(type(есть_ребёнок)) # <class 'bool'>

# ------ БЛОК 4: Математика ------
a = 10
b = 3

print(a + b)   # 13  — сложение
print(a - b)   # 7   — вычитание
print(a * b)   # 30  — умножение
print(a / b)   # 3.33 — деление (всегда float)
print(a // b)  # 3   — целочисленное деление
print(a % b)   # 1   — остаток от деления
print(a ** b)  # 1000 — возведение в степень


