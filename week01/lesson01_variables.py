# Переменные с разными типами данных
name = "Руслан"
age = 33
city = "Уфа"
salary = 80000.0
is_employed = True

# Простой вывод
print(name)
print(age)

# Вывод нескольких значений через запятую
print("Имя:", name, "| Возраст:", age)

# f-строка — самый удобный способ
print(f"Меня зовут {name}, мне {age} лет, я из {city}")
print(f"Зарплата: {salary} руб.")
print(f"Работает: {is_employed}")