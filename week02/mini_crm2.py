print(d.client, d.amount)


class CRM:
    def __init__(self):
        self.deals = []

    def add_deal(self, deal):
        self.deals.append(deal)

    def report(self):
        total = 0
        for deal in self.deals:
            total += deal.amount
        print (f"Сделок: {len(self.deals)}, выручка: {total}")

            

            

    
