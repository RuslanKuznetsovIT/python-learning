# ЗАДАЧА — корзина интернет-магазина
# ------------------------------------------------------------
# Есть корзина покупок. У неё есть владелец (имя) и сумма заказа,
# которая стартует с 0.
#
# Корзина умеет:
#   - добавлять товар по цене N: увеличивает сумму заказа на N   (ИЗМЕНИТЕЛЬ)
#   - оформлять заказ (checkout): смотрит на сумму и решает:
#       * если сумма >= 1000 -> "бесплатная доставка"
#       * иначе              -> "доставка 300 руб"               (ЧИТАТЕЛЬ-С-РЕШЕНИЕМ)
#
# Сценарий:
#   Создай корзину для "Иван".
#   Добавь товар за 600, потом товар за 500.   (сумма станет 1100)
#   Оформи заказ и выведи: "Иван: бесплатная доставка"
#
# Ожидаемый вывод:
#   Иван: бесплатная доставка

# твой код:

class Basket:
    def __init__(self, name):
        self.name = name
        self.summ = 0
    
    def up_summ(self, n):
        self.summ += n

    def checkout(self):
        if self.summ >= 1000:
            return f'{self.name}: бесплатная доставка'
        else:
            return f'{self.name}: доставка 300'
        
b = Basket('Иван')
b.up_summ(600)
b.up_summ(500)
print(b.checkout())
        