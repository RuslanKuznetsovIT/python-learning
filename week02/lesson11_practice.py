# ------------------------------------------------------------
# ЗАДАЧА 1 (разминка) — класс с __init__
# ------------------------------------------------------------
# Создай класс Product с __init__(self, name, price).
# Сделай один объект: Product("Ноутбук", 50000).
# Выведи: "Товар: Ноутбук, цена: 50000"
#
# Ожидаемый вывод:
#   Товар: Ноутбук, цена: 50000

# твой код:
class Product:
    def __init__(self, name, price):
        self.name = name
        self.price = price
p = Product('Ноутбук', 50000)
print(f'Товар: {p.name}, цена: {p.price}')



# ------------------------------------------------------------
# ЗАДАЧА 2 (средняя) — много объектов в списке
# ------------------------------------------------------------
# Создай класс Student с __init__(self, name, grade).
# Сделай ТРИ студента и положи их в список:
#   Анна — 5, Игорь — 4, Света — 3
# Пройди список циклом и выведи каждого в формате "имя: оценка".
#
# Ожидаемый вывод:
#   Анна: 5
#   Игорь: 4
#   Света: 3

# твой код:
class Student:
    def __init__(self, name,grade):
        self.name = name
        self.grade = grade


s_1 = Student('Анна', 5)
s_2 = Student('Игорь', 4)
s_3 = Student('Света', 3)

students = [s_1, s_2, s_3]
for student in students:
    print(f"{student.name}: {student.grade}")


# ------------------------------------------------------------
# ЗАДАЧА 3 (со звёздочкой) — список объектов + накопитель
# ------------------------------------------------------------
# Создай класс Deal с __init__(self, client, amount).
# Сделай список из трёх сделок:
#   Иванов — 150000, Петрова — 80000, Сидоров — 200000
# Пройди список циклом и посчитай ОБЩУЮ сумму всех сделок.
# Выведи: "Общая сумма: <итог>"
#
# Подсказка по структуре: это твой старый паттерн-накопитель,
# только теперь в списке лежат ОБЪЕКТЫ, и к сумме обращаешься
# через точку: deal.amount (а не deal["amount"]).
#
# Ожидаемый вывод:
#   Общая сумма: 430000

# твой код:

class Deal:
    def __init__(self, client, amount):
        self.client = client
        self.amount = amount

d_1 = Deal('Иванов', 150000)
d_2 = Deal('Петрова', 80000)
d_3 = Deal('Сидоров', 200000)

deals = [d_1, d_2, d_3]

total = 0
for deal in deals:
    total += deal.amount

print(f'Общая сумма: {total}')