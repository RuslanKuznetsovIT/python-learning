# Есть плейлист. У него есть название и список треков,
# который стартует ПУСТЫМ.
#
# Плейлист умеет:
#   - добавлять трек по названию: кладёт его в свой список   (ИЗМЕНИТЕЛЬ, .append)
#   - показывать, сколько всего треков                       (ЧИТАТЕЛЬ, len)
#
# Сценарий:
#   Создай плейлист "Любимое".
#   Добавь три трека: "Song1", "Song2", "Song3".
#   Выведи: "Любимое: 3 трека"
#
# Ожидаемый вывод:
#   Любимое: 3 трека

# твой код:
class Playlist:
    def __init__(self, name):
        self.name = name
        self.treks = []
    
    def plus_trek(self, trek):
        self.treks.append(trek)

    def count(self):
        return len(self.treks)
    
p = Playlist('Любимое')
p.plus_trek("Song1")
p.plus_trek("Song2")
p.plus_trek("Song3")
print(f'{p.name}: {p.count()} трека')