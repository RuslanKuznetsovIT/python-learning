# ЗАДАЧА:
# Напиши функцию safe_sum(items), которая принимает список и
# возвращает СУММУ всех элементов, которые являются числами,
# а мусор (текст) пропускает.
#
# Структура (вспомни скелет):
#   total = 0 внутри функции
#   for по items
#       try: прибавить int(x) к total
#       except ValueError: pass
#   return total
#   снаружи — вызвать и вывести
#
# Ожидаемый вывод:
#   Сумма: 350

items = ["100", "оплата", "50", "200", "ошибка"]

# твой код:
def safe_sum(items):
    total = 0
    for item in items:
        try:
            total = total + int(item)
        except ValueError:
            pass
    return total

print(f"    Сумма: {safe_sum(items)}")