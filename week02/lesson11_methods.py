# ------------------------------------------------------------
# ЗАДАЧА — метод, который считает комиссию
# ------------------------------------------------------------
# 1. Создай класс Deal с __init__(self, client, amount).
# 2. Добавь МЕТОД commission(self), который возвращает 10% от суммы
#    (amount * 0.1). Метод берёт сумму из СВОИХ данных через self.
# 3. Создай сделку: Deal("Иванов", 150000).
# 4. Выведи: "Комиссия: 15000.0"
#
# Ожидаемый вывод:
#   Комиссия: 15000.0

# твой код:
class Deal:
    def __init__(self, client, amount):
        self.client = client
        self.amount = amount

    def commission(self):
        return f"Комиссия: {self.amount * 0.1}"

d = Deal("Иванов", 150000)
print(d.commission())