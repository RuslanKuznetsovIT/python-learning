# Сделай класс Player (игрок):
#   __init__(self, name) — принимает имя; здоровье health стартует со 100.
#   метод take_damage(self, amount) — уменьшает health на amount (self.health -= amount).
#
# Создай игрока Player("Рыцарь").
# Нанеси урон два раза: 30, потом 20.
# Выведи: "Рыцарь: здоровье 50"
#
# Ожидаемый вывод:
#   Рыцарь: здоровье 50

# твой код:

class Player:
    def __init__(self, name):
        self.name = name
        self.health = 100

    def take_damage(self, amount):
        self.health  -= amount


p = Player('Рыцарь')
p.take_damage(30)
p.take_damage(20)
print(f"{p.name}: здоровье {p.health}")
