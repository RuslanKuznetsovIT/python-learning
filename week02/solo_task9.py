# Есть ноутбук. У него есть название и заряд, который
# ВСЕГДА стартует со 100.
#
# Ноутбук умеет:
#   - работать N часов: каждый час съедает 10% заряда      (ИЗМЕНИТЕЛЬ)
#       (т.е. заряд уменьшается на hours * 10)
#   - показывать статус: если заряд > 0 -> "рабочий",
#                        иначе -> "разряжен"               (ЧИТАТЕЛЬ-С-РЕШЕНИЕМ)
#
# Создай ноутбук "Asus".
# Поработай 4 часа, потом ещё 5 часов.
# Выведи статус в формате: "Asus: рабочий"
#
# Ожидаемый вывод:
#   Asus: рабочий
#
# (100 - 4*10 - 5*10 = 10  ->  10 > 0  ->  "рабочий")

# твой код:

class Notebook:
    def __init__(self, model):
        self.model = model
        self.charge = 100

    def work(self, hours):
        self.charge -= hours * 10

    def status(self):
        if self.charge > 0:
           return f'{self.model}: рабочий'
        else:
            return f'{self.model}: разряжен'

n = Notebook('Asus')
n.work(4)
n.work(5)
print(n.status())