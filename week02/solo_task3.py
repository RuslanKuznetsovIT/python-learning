# Есть посылка. У неё есть получатель (имя) и вес (в кг).
# Нужно уметь посчитать стоимость доставки: вес × 50 рублей за кг.
#
# Создай посылку для "Иванов" весом 3 кг.
# Выведи: "Иванов: доставка 150 руб"
#
# Ожидаемый вывод:
#   Иванов: доставка 150 руб

# твой код (но СНАЧАЛА — 5 шагов рассуждения мне в чат):

class Parcel:
    def __init__(self, name, weight):
        self.name = name
        self.weight = weight
    def delivery_cost(self):
        return self.weight * 50

p = Parcel('Иванов', 3)
print(f'{p.name}: доставка {p.delivery_cost()} руб')