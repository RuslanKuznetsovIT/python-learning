# Есть банковский счёт. У него есть владелец (имя) и баланс,
# который стартует с 0.
#
# Счёт умеет:
#   - пополняться на N (просто увеличивает баланс).
#   - снимать N, НО:
#       * если на счету ХВАТАЕТ (баланс >= N) — снимает (баланс уменьшается);
#       * если НЕ хватает — снятие НЕ происходит, баланс остаётся прежним.
#   - показывать баланс.
#
# Сценарий:
#   Создай счёт для "Иван".
#   Пополни на 1000.
#   Сними 300.     (хватает -> баланс станет 700)
#   Сними 5000.    (НЕ хватает -> баланс остаётся 700)
#   Выведи: "Иван: баланс 700"
#
# Ожидаемый вывод:
#   Иван: баланс 700
#
# (если снятие на 5000 пройдёт по ошибке — баланс уйдёт в минус,
#  и ты увидишь это, сверив с эталоном. Дисциплина: запусти -> сверь.)

# твой код:
class Bank_account:
    def __init__(self, name):
        self.name = name
        self.balance = 0

    def up_balance(self, n):
        self.balance += n
    
    def down_balance(self, take_n):
        if self.balance >= take_n:
            self.balance -= take_n
        else:
            pass



b = Bank_account('Иван')
b.up_balance(1000)
b.down_balance(300)
b.down_balance(5000)

print(f'{b.name}: баланс {b.balance}')
