# ------------------------------------------------------------
# ЗАДАЧА 1 (разминка) — метод читает свои данные и считает
# ------------------------------------------------------------
# Класс Employee, __init__(self, name, salary).
# Метод yearly(self) — возвращает годовую зарплату (salary * 12).
# Создай Employee("Анна", 80000), выведи "Годовая: 960000".
#
# Ожидаемый вывод:
#   Годовая: 960000

# твой код:
class Employee:
    def __init__(self, name, salary):
        self.name = name
        self.salary = salary

    def yearly(self):
        return f'Годовая: {self.salary * 12}'

e = Employee('Анна', 80000)
print(e.yearly())




