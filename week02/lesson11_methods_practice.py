# ЗАДАЧА 3 (со звёздочкой) — метод МЕНЯЕТ данные объекта
# ------------------------------------------------------------
# Новое: метод может не только читать, но и ИЗМЕНЯТЬ свои поля (self.count = ...).
#
# Класс Counter, __init__(self) — БЕЗ доп. параметров, просто ставит self.count = 0.
# Метод increment(self) — увеличивает self.count на 1.
# Создай счётчик, вызови increment() ТРИ раза, выведи "Счётчик: 3".
#
# Ожидаемый вывод:
#   Счётчик: 3

# твой код:
class Counter:
    def __init__(self):
        self.count = 0

    def increment(self):
        self.count += 1

c = Counter()
c.increment()
c.increment()
c.increment()
print(f'Счётчик: {c.count}')
