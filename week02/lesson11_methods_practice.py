# ЗАДАЧА 2 (средняя) — метод с ДОПОЛНИТЕЛЬНЫМ параметром
# ------------------------------------------------------------
# Новое: метод может принимать не только self, но и ещё параметры.
#   def can_afford(self, price):   <- self ПЕРВЫМ, потом price
# Вызов: acc.can_afford(3000)      <- self подставится сам, ты передаёшь только price
#
# Класс BankAccount, __init__(self, owner, balance).
# Метод can_afford(self, price) — возвращает True, если balance >= price, иначе False.
# Создай счёт с balance = 5000. Выведи результат двух проверок: на 3000 и на 8000.
#
# Ожидаемый вывод:
#   True
#   False

# твой код:
class BankAccount:
    def __init__(self, owner, balance):
        self.balance = balance
        
    def can_afford(self, price):
        return self.balance >= price
    
acc = BankAccount("Ann", 5000)
print(acc.can_afford(3000))
print(acc.can_afford(8000))


