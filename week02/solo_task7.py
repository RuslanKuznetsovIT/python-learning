# Есть студент. У него есть имя и балл за экзамен.
# Студент умеет сказать результат: если балл >= 60 — "Сдал",
# иначе — "Не сдал".
#
# Создай двух студентов:
#   "Анна" с баллом 75
#   "Игорь" с баллом 45
# Выведи для каждого: "имя: результат"
#
# Ожидаемый вывод:
#   Анна: Сдал
#   Игорь: Не сдал
#
# Подумай по схеме:
#   шаг 3 — метод результата: он ЧИТАЕТ или МЕНЯЕТ? и есть ли тут РЕШЕНИЕ (if)?

# твой код:

class Student:
    def __init__(self, name, ball):
        self.name = name
        self.ball = ball

    def result(self):
        if self.ball >= 60:
            return f'{self.name}: Сдал'
        else:
            return f'{self.name}: Не сдал'
        

s_1 = Student('Анна',75)
s_2 = Student('Игорь', 45)

print(s_1.result())
print(s_2.result())