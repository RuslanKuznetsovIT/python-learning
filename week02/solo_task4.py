# Есть термостат в комнате. У него есть название комнаты,
# и температура, которая ВСЕГДА стартует с 20 градусов.
# Термостат умеет подогревать: увеличивать температуру на N градусов.
#
# Создай термостат для комнаты "Спальня".
# Подогрей два раза: на 3 градуса, потом на 2.
# Выведи: "Спальня: температура 25"
#
# Ожидаемый вывод:
#   Спальня: температура 25

# твой код (но СНАЧАЛА — 5 шагов рассуждения мне в чат):

class Termostat:
    def __init__(self, room):
        self.room = room
        self.temp = 20
    def up_temp(self,temp):
        self.temp += temp

t = Termostat("Спальня")
t.up_temp(2)
t.up_temp(3)
print(f'{t.room}: температура {t.temp}')
