# Дан список задач, у каждой есть текст и статус "выполнена" (done).
# Напиши функцию count_done(tasks), которая возвращает,
# СКОЛЬКО задач выполнено (done == True).
#
# Подсказка по структуре (всё знакомое):
#   счётчик = 0 внутри функции
#   for по tasks
#       if task["done"] == True (или просто if task["done"])
#           счётчик += 1
#   return счётчик
#   снаружи — вызвать и вывести
#
# Ожидаемый вывод:
#   Выполнено задач: 3

tasks = [
    {"task": "Позвонить клиенту", "done": True},
    {"task": "Отправить счёт", "done": False},
    {"task": "Обновить CRM", "done": True},
    {"task": "Написать отчёт", "done": True},
]

# твой код:
def count_done(tasks):
    count = 0
    for task in tasks:
        if task['done'] == True:
            count += 1
    return count
print(f"   Выполнено задач: {count_done(tasks)}")