# Есть работник. У него есть имя и ставка за час (сколько он получает за 1 час).
# Работник умеет посчитать зарплату за отработанные часы: ставка × часы.
#
# Создай работника "Иван" со ставкой 500.
# Посчитай зарплату за 8 часов.
# Выведи: "Иван заработал: 4000 руб"
#
# Ожидаемый вывод:
#   Иван заработал: 4000 руб
#
# Подсказка-вопрос (для шага 3): метод считает зарплату — он
# ЧИТАЕТ данные и отдаёт число, или МЕНЯЕТ объект? От этого — return или нет.

# твой код:
class Worker:
    def __init__(self, name, rate):
        self.name = name
        self.rate = rate

    def salary(self, hour):
        return  self.rate * hour

w = Worker("Иван", 500)
print(f'{w.name} заработал: {w.salary(8)} руб')