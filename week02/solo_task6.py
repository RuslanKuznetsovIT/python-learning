# Есть телефон. У него есть модель и заряд батареи,
# который ВСЕГДА стартует с 0.
# Телефон умеет заряжаться: увеличивать заряд на N процентов.
#
# Создай телефон "iPhone".
# Заряди два раза: на 30, потом на 40.
# Выведи: "iPhone: заряд 70%"
#
# Ожидаемый вывод:
#   iPhone: заряд 70%
#
# Подумай (шаг 3): метод зарядки — ЧИТАЕТ или МЕНЯЕТ?
#   → от этого: будет return или self.что-то меняется;
#   → и в print: вызывать метод или читать поле?

# твой код:

class Phone:
    def __init__(self, model):
        self.model = model
        self.battary = 0

    def up_charge(self, battary):
        self.battary += battary

p = Phone('Iphone')
p.up_charge(30)
p.up_charge(40)
print(f'{p.model}: заряд {p.battary}%')