# Дан список заказов:
print(f"Список заказов:")
orders = [
    {"client": "Иванов", "product": "Ноутбук", "amount": 75000, "city": "Москва"},
    {"client": "Петрова", "product": "Телефон", "amount": 45000, "city": "Уфа"},
    {"client": "Сидоров", "product": "Планшет", "amount": 30000, "city": "Москва"},
    {"client": "Кузнецова", "product": "Ноутбук", "amount": 90000, "city": "Казань"},
    {"client": "Орлов", "product": "Наушники", "amount": 12000, "city": "Уфа"}
]
#
# Шаги:
#
# 1. Выведи заголовок: "Список заказов:"
#    Через цикл выведи каждый заказ в формате:
#    "  Клиент → Продукт (Сумма ₽, Город)"
#    Например: "  Иванов → Ноутбук (75000 ₽, Москва)"
#
# 2. Через накопитель посчитай ОБЩУЮ ВЫРУЧКУ всех заказов.
#    Выведи: "Общая выручка: X ₽"
#
# 3. Через счётчик посчитай, СКОЛЬКО заказов из Уфы.
#    Выведи: "Заказов из Уфы: X"
#
# 4. Через накопитель с условием посчитай сумму заказов на Ноутбук.
#    Выведи: "Выручка с ноутбуков: X ₽"
#
# Ожидаемый вывод:
# Список заказов:
#   Иванов → Ноутбук (75000 ₽, Москва)
#   Петрова → Телефон (45000 ₽, Уфа)
#   Сидоров → Планшет (30000 ₽, Москва)
#   Кузнецова → Ноутбук (90000 ₽, Казань)
#   Орлов → Наушники (12000 ₽, Уфа)
# Общая выручка: 252000 ₽
# Заказов из Уфы: 2
# Выручка с ноутбуков: 165000 ₽
#
# Дисциплина: запусти → сверь посимвольно → потом сдавай.

# Пиши код ниже (список orders уже создан выше):

for order in orders:
    print(f"  {order['client']} → {order['product']} ({order['amount']} ₽, {order['city']})")

total_sales = 0
for order in orders:
    total_sales = total_sales + order["amount"]
print(f"Общая выручка: {total_sales} ₽")

total_city = 0
for order in orders:
    if order["city"] == 'Уфа':
        total_city += 1
print(f"Заказов из Уфы: {total_city}")


total_notebook = 0
for order in orders:
    if order["product"] == 'Ноутбук':
        total_notebook = total_notebook + order["amount"]

print(f"Выручка с ноутбуков: {total_notebook} ₽")