# Есть банковский счёт. У него есть владелец (имя) и баланс,
# который ВСЕГДА стартует с 0.
#
# Счёт умеет:
#   - пополняться: увеличивать баланс на N            (это ИЗМЕНИТЕЛЬ)
#   - показывать статус: если баланс >= 100000 -> "богатый",
#                        иначе -> "бедный"            (это ЧИТАТЕЛЬ-С-РЕШЕНИЕМ)
#
# Создай счёт для "Иван".
# Пополни два раза: на 50000, потом на 60000.
# Выведи статус в формате: "Иван: богатый"
#
# Ожидаемый вывод:
#   Иван: богатый
#
# (баланс станет 110000 -> это >= 100000 -> "богатый")

# твой код:

class Bank_account:
    def __init__(self, name):
        self.name = name
        self.balance = 0
    
    def up_accont(self, n):
        self.balance += n

    def status_account(self):
        if self.balance >= 100000:
            return f'{self.name}: богатый'
        else:
            return f'{self.name}: бедный'
        
ac = Bank_account('Иван')
ac.up_accont(50000)
ac.up_accont(60000)

print(f"{ac.status_account()}")