# Задание: отчёт по бонусам менеджеров отдела.
#
# 1. Создай словарь bonuses, где ключ — имя менеджера, значение — процент бонуса:
#    Анна: 25
#    Сергей: 30
#    Виктор: 15
#    Мария: 20
#
# 2. Выведи заголовок "Бонусы менеджеров:"
#
# 3. Через цикл for ... in bonuses.items() выведи каждого менеджера в формате:
#    "  Имя: ПРОЦЕНТ%"
#    (с двумя пробелами в начале для отступа)
#
# 4. Через цикл for ... in bonuses.values() с накопителем посчитай СУММУ всех процентов
#    Выведи: "Сумма процентов: X"
#
# 5. Посчитай СРЕДНИЙ процент (сумма поделить на количество менеджеров).
#    Используй len(bonuses) для количества.
#    Выведи: "Средний бонус: Y.Z%"
#
# Ожидаемый вывод:
# Бонусы менеджеров:
#   Анна: 25%
#   Сергей: 30%
#   Виктор: 15%
#   Мария: 20%
# Сумма процентов: 90
# Средний бонус: 22.5%
#
# Дисциплина: запусти → сверь посимвольно → потом сдавай.

# Пиши код ниже:

print('Бонусы менеджеров:')
bonuses = {
    'Анна': 25,
    'Сергей': 30,
    'Виктор': 15,
    'Мария': 20
}

for name,percent in bonuses.items():
    print(f"  {name}: {percent}%")



sum_percent = 0
for percent_of_manager in bonuses.values():
    sum_percent = sum_percent + percent_of_manager
        

print(f"Сумма процентов: {sum_percent}")


print(f"Средний бонус: {sum_percent / len(bonuses)}%")