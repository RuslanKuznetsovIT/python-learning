# ------------------------------------------------------------
# ЗАДАЧА 1 (разминка) — просто класс с __init__
# ------------------------------------------------------------
# Создай класс Cat с __init__(self, name, age).
#   name — кличка (строка)
#   age  — возраст (число)
#
# Создай одного кота: Cat("Барсик", 3)
# Выведи строкой: "Кота зовут Барсик, ему 3 лет"
#
# Ожидаемый вывод:
#   Кота зовут Барсик, ему 3 лет

# твой код:
class Cat:
    def __init__(self, name, age):
        self.name = name
        self.age = age
    def voice(self):
        return f"{self.name} говорит: Мяу!"
    
    def birthday(self):
        self.age = self.age + 1

        return f"{self.name} теперь {self.age} лет"

cat = Cat("Барсик", 3)
cat_2 = Cat('Piksi', 10)
print(f'Кота зовут {cat.name}, ему {cat.age} лет')


# ------------------------------------------------------------
# ЗАДАЧА 2 — дадим коту СПОСОБНОСТЬ (метод)
# ------------------------------------------------------------
# Добавь в класс Cat метод voice(self), который ВОЗВРАЩАЕТ строку
# вида "Барсик говорит: Мяу!" — имя кот берёт из СВОИХ данных (self.name).
#
# Напоминание формы метода:
#   - пишется ВНУТРИ класса (отступ как у __init__)
#   - self ПЕРВЫМ в скобках
#   - вызывается через cat.voice()  (со скобками — это действие)
#
# Вызови метод и выведи результат.
#
# Ожидаемый вывод:
#   Барсик говорит: Мяу!

# твой код (метод voice допиши ВНУТРЬ класса Cat выше, вызов — здесь):
print(cat.voice())


# ------------------------------------------------------------
# ЗАДАЧА 3 — метод, который МЕНЯЕТ данные (день рождения)
# ------------------------------------------------------------
# Добавь в класс Cat метод birthday(self), который увеличивает
# возраст кота на 1 (self.age += 1). Метод ничего не возвращает —
# он просто меняет поле объекта.
#
# Барсику сейчас 3. Позови birthday() ОДИН раз, потом выведи возраст.
#
# Ожидаемый вывод:
#   Барсику теперь 4 лет

# твой код (метод birthday допиши ВНУТРЬ класса Cat, вызов и вывод — здесь):

print(cat.birthday())
print(cat_2.birthday())