# --- ПРИМЕР (разбери построчно) ---

class Deal:          # ЧЕРТЁЖ (класс). Имя класса — с БОЛЬШОЙ буквы (соглашение).
    pass             # пока пустой чертёж, просто "тут будет сделка"


# Лепим ОБЪЕКТ по чертежу:
deal = Deal()        # скобки () = "сделай мне одну сделку по этому чертежу"

# Даём этому объекту его данные (атрибуты) через ТОЧКУ:
deal.client = "Иванов"
deal.amount = 150000

# Читаем обратно — тоже через точку:
print(deal.client)   # Иванов
print(deal.amount)   # 150000

# Точка "." = залезть внутрь объекта (похоже на [] у словаря, но для объектов).


