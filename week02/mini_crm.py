# ШАГ 2 — класс CRM (хранит сделки + умеет добавлять)
# ------------------------------------------------------------
# По образцу "корзины" (Basket) из объяснения:
#   __init__(self) — БЕЗ параметров, внутри пустой список: self.deals = []
#   add_deal(self, deal) — кладёт сделку в self.deals через .append()
#
# Проверка: создай CRM, добавь ТРИ сделки, выведи их количество.
#   crm = CRM()
#   crm.add_deal(Deal("Иванов", 150000, "закрыта"))
#   crm.add_deal(Deal("Петрова", 80000, "в работе"))
#   crm.add_deal(Deal("Сидоров", 200000, "закрыта"))
#   print(f"Сделок в CRM: {len(crm.deals)}")
#
# Ожидаемый вывод:
#   Сделок в CRM: 3

# твой код:

class CRM:
    def __init__(self):
        self.deals = []

    def add_deal(self, deal):
        self.deals.append(deal)
    
    def report(self):
        total = 0
        for n in self.deals:
            if n.stage == 'закрыта':
                total += n.amount
                print(f'Сделок всего: {len(self.deals)}')
                print(f'Выручка по закрытым: {total}')



crm = CRM()
crm.add_deal(Deal("Иванов", 150000, "закрыта"))
crm.add_deal(Deal("Петрова", 80000, "в работе"))
crm.add_deal(Deal("Сидоров", 200000, "закрыта"))
print(f"Сделок в CRM: {len(crm.deals)}")

print("--- Сделки внутри CRM: ---")
for deal in crm.deals:
    print(deal.client, deal.amount)


