# Урок 7 (разогрев) — собираем всё вместе
# Задание: посчитать "бонусный пул" компании.
#
# В компании за месяц закрыты сделки. Бонусы выплачиваются только за сделки,
# которые равны или превышают определённый порог.
#
# 1. Опиши функцию bonus_pool(deals, threshold), которая:
#    - принимает список сделок (deals) и пороговое значение (threshold)
#    - проходит по списку циклом
#    - суммирует только те сделки, которые >= threshold
#    - возвращает итоговую сумму
#
# 2. Создай список сделок:
#    deals = [50000, 120000, 80000, 35000, 150000, 25000]
#
# 3. Вызови функцию три раза с разными порогами:
#    - threshold = 50000
#    - threshold = 100000
#    - threshold = 200000
#
# 4. Выведи результаты:
#    Пул с порогом 50000: 400000
#    Пул с порогом 100000: 270000
#    Пул с порогом 200000: 0
#
# Проверка по математике:
#  50000 + 120000 + 80000 + 150000 = 400000   (всё кроме 35000 и 25000)
#  120000 + 150000 = 270000                    (только два больших)
#  ни одна сделка не >= 200000                 → 0
#
# Дисциплина: запусти → сверь посимвольно → потом сдавай.

# Пиши код ниже:

def bonus_pool(deals, threshold):
    total = 0
    for deal in deals:
        if deal >= threshold:
            total = total + deal
    return total

deals = [50000, 120000, 80000, 35000, 150000, 25000]

result_50 = bonus_pool(deals, 50000)
result_100 = bonus_pool(deals, 100000)
result_200 = bonus_pool(deals, 200000)


print(f"Пул с порогом 50000: {result_50}")
print(f"Пул с порогом 100000: {result_100}")
print(f"Пул с порогом 200000: {result_200}")