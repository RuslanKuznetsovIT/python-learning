#   (например, скидка 20% от 1000 → цена станет 800)
#
# Создай товар Product("Книга", 1000).
# Выведи цену со скидкой 20% в формате:
#   "Цена со скидкой: 800.0"
#
# Ожидаемый вывод:
#   Цена со скидкой: 800.0

# твой код:

class Product:
    def __init__(self, name, price):
        self.name = name
        self.price = price

    def with_discount(self, percent):
        coast = self.price - self.price * percent
        return f'Цена со скидкой: {coast}'

product_price = Product("Книга", 1000)
print(product_price.with_discount(0.2))