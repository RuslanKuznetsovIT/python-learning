# Урок 7 — словари: тренировка синтаксиса
# Три упражнения, каждое короче предыдущего.
# Цель — чтобы фигурные скобки, двоеточия и квадратные скобки стали автоматическими.

# ===============================================
# УПРАЖНЕНИЕ 1 — создай и прочитай
# ===============================================
# Создай словарь deal со следующими полями:
#   id: 1042
#   client: "ООО Ромашка"
#   amount: 350000
#   status: "новая"
#
# Выведи:
#   Сделка 1042
#   Клиент: ООО Ромашка
#   Сумма: 350000
#   Статус: новая

# твой код:

deal = {
    "id": 1042,
    "client": "ООО Ромашка",
    "amount": 350000,
    "status": "новая"
}
print(f"Сделка {deal['id']}")
print(f"Клиент: {deal['client']}")
print(f"Сумма: {deal['amount']}")
print(f"Статус: {deal['status']}")



# ===============================================
# УПРАЖНЕНИЕ 2 — измени и добавь
# ===============================================
# Возьми тот же deal и:
# - поменяй статус на "в работе"
# - добавь поле manager со значением "Сергей"
#
# Выведи итог в одной строке через f-строку:
#   Сделка 1042: статус "в работе", менеджер Сергей

# твой код:

deal["status"] = '"в работе"'
deal["manager"] = "Сергей"

print(f"Сделка {deal['id']}: статус {deal['status']}, менеджер {deal['manager']}")

# ===============================================
# УПРАЖНЕНИЕ 3 — проверка ключей
# ===============================================
# Используя оператор in, проверь есть ли в deal:
