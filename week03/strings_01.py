# Без классов. Методы: .split() + len()
# ============================================================
#
# Напоминание:
#   "a,b,c".split(",")  ->  ['a', 'b', 'c']   (строка -> список)
#
# ------------------------------------------------------------
# ЗАДАЧА
# ------------------------------------------------------------
# Дана строка-список покупок (товары через запятую):
products = "хлеб,молоко,яйца,сыр"
#
# 1. Разрежь строку в список товаров через .split(",")
# 2. Посчитай, сколько товаров (len)
# 3. Выведи: "Продуктов: 4"
#
# Ожидаемый вывод:
#   Продуктов: 4

# твой код:

a = len(products.split(","))
print(f"Продуктов: {a}")


# ------------------------------------------------------------
# ЗАДАЧА 2 — чистка ввода (.strip + .lower, цепочкой)
# ------------------------------------------------------------
# Пользователь ввёл почту криво — с пробелами по краям и в разном регистре.
# Нужно её "нормализовать": убрать пробелы по краям И сделать всё строчными.
#
raw_email = "  Ivan@Mail.RU  "
#
# Сделай из неё: "ivan@mail.ru"
# Подсказка: методы можно вызывать цепочкой — strip(), потом lower().
#
# Ожидаемый вывод:
#   ivan@mail.ru

# твой код:

print(raw_email.strip().lower())


# ------------------------------------------------------------
# ЗАДАЧА 3 — чистка номера телефона (.replace)
# ------------------------------------------------------------
# Телефон ввели с дефисами, а нужны только цифры подряд.
phone = "8-900-123-45-67"
#
# Убери ВСЕ дефисы "-" и выведи: "89001234567"
# Подсказка: .replace(что_убрать, на_что) — а "убрать" = заменить на пустую строку "".
#
# Ожидаемый вывод:
#   89001234567

# твой код:
print(phone.replace("-", ""))


# ------------------------------------------------------------
# ЗАДАЧА 4 — разобрать ФИО (.split + индексы [0], [1])
# ------------------------------------------------------------
full_name = "Иван Иванов"
#
# 1. Разрежь строку по ПРОБЕЛУ через .split(" ")  ->  получишь список из 2 слов
# 2. Возьми имя = [0], фамилию = [1]
# 3. Выведи: "Имя: Иван, Фамилия: Иванов"
#
# Ожидаемый вывод:
#   Имя: Иван, Фамилия: Иванов

# твой код:


print(f'Имя: {full_name.split(" ")[0]}, Фамилия: {full_name.split(" ")[1]}')
