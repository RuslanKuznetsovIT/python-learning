# ЗАДАЧА
# ------------------------------------------------------------
# 1. Запиши в файл "note.txt" строку: "Меня зовут Руслан"
#    (режим "w", encoding="utf-8")
# 2. Потом ОТКРОЙ этот же файл на ЧТЕНИЕ ("r"), прочитай содержимое
# 3. Выведи прочитанное с префиксом: "Из файла: Меня зовут Руслан"
#
# Ожидаемый вывод:
#   Из файла: Меня зовут Руслан

# твой код:

with open("note.txt", "w", encoding="utf-8") as f:
    f.write('Меня зовут Руслан')

with open("note.txt", "r", encoding="utf-8") as f:
    content = f.read()

print(f'Из файла: {content}')