# Дата в виде строки формата ГГГГ-ММ-ДД:
date = "2026-06-28"
#       индексы: 2026 = [0:4],  06 = [5:7],  28 = [8:10] (или [8:])
#
# Вырежь срезами год, месяц и день и выведи:
#   "Год: 2026, Месяц: 06, День: 28"
#
# Ожидаемый вывод:
#   Год: 2026, Месяц: 06, День: 28

# твой код:
a = date.replace("-", "")
print(f'Год: {a[0:4]}, Месяц: {a[4:6]}, День: {a[6:]}')

print(f'Год: {date.replace("-", " ")[0:4]}, Месяц: {date.replace("-", " ")[5:7]}, День: {date.replace("-", " ")[8:]}')

print(f"Год: {date[0:4]}, Месяц: {date[5:7]}, День: {date[8:]}")