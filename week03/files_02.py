# 2. Открой файл на чтение и пройди ПОСТРОЧНО (for line in f),
#    выведи каждое дело (убери лишний перевод строки через .strip()).
#
# Ожидаемый вывод:
#   Купить хлеб
#   Позвонить маме
#   Сделать урок

# твой код:
with open("todo.txt", "w", encoding="utf-8") as f:
    f.write("Купить хлеб\n")
    f.write("Позвонить маме\n")
    f.write("Сделать урок\n")

with open('todo.txt', 'r', encoding="utf-8") as f:
    for line in f:
        print(line.strip())